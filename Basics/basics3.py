no1 = input("Enter a number")
print("Value of no1:", no1)
print("Type of no1:", type(no1))

no2 = int(input("Enter a number"))
print("Value of no1:", no2)
print("Type of no1:", type(no2))

# If we enter integer as input, it will be converted to float implicitly
no3 = float(input("Enter a number"))
print("Value of no1:", no3)
print("Type of no1:", type(no3))
